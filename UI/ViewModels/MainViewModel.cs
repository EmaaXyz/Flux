using System;
using System.Collections.ObjectModel;
using System.ComponentModel;
using System.Runtime.CompilerServices;
using System.Threading.Tasks;
using System.Windows.Input;
using Flux.Core.Engine;
using Flux.Core.Models;
using Flux.Core.Services;

namespace Flux.UI.ViewModels
{
    /// <summary>
    /// MainViewModel ??? the single source of truth for the main window.
    /// Bridges HardwareEngine events ??? observable WPF properties.
    /// </summary>
    public sealed class MainViewModel : INotifyPropertyChanged, IDisposable
    {
        // ?????? Services ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        private readonly HardwareEngine   _engine;
        private readonly GamingModeManager _gamingMode;

        // ?????? Constructor ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        public MainViewModel()
        {
            _engine     = new HardwareEngine(pollingIntervalMs: 1000);
            _gamingMode = new GamingModeManager();

            _engine.HardwareDataUpdated      += OnHardwareDataUpdated;
            _gamingMode.StateChanged         += OnGamingModeStateChanged;
            _gamingMode.LogMessage += msg => System.Windows.Application.Current.Dispatcher.Invoke(() => GamingLog.Insert(0, msg));

            ToggleGamingModeCommand = new AsyncRelayCommand(ToggleGamingModeAsync);

            _engine.Start();
        }

        // ?????? CPU Properties ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        private float _totalCpuLoad;
        public float TotalCpuLoad
        {
            get => _totalCpuLoad;
            set => Set(ref _totalCpuLoad, value);
        }

        public ObservableCollection<CoreLoadItem> CoreLoads { get; } = new();

        // ?????? RAM Properties ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        private float _ramUsagePercent;
        public float RamUsagePercent
        {
            get => _ramUsagePercent;
            set => Set(ref _ramUsagePercent, value);
        }

        private string _ramUsageLabel = "??? / ??? GB";
        public string RamUsageLabel
        {
            get => _ramUsageLabel;
            set => Set(ref _ramUsageLabel, value);
        }

        // ?????? GPU Properties ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        private string _gpuName = "Detecting???";
        public string GpuName
        {
            get => _gpuName;
            set => Set(ref _gpuName, value);
        }

        private float _gpuTemperature = -1;
        public float GpuTemperature
        {
            get => _gpuTemperature;
            set => Set(ref _gpuTemperature, value);
        }

        public string GpuTempLabel =>
            GpuTemperature < 0 ? "N/A" : $"{GpuTemperature:F0} ?C";

        // ?????? Gaming Mode ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        private GamingModeState _gamingModeState = GamingModeState.Off;
        public GamingModeState GamingModeState
        {
            get => _gamingModeState;
            set
            {
                Set(ref _gamingModeState, value);
                OnPropertyChanged(nameof(IsGamingModeActive));
                OnPropertyChanged(nameof(GamingModeButtonLabel));
            }
        }

        public bool   IsGamingModeActive    => _gamingModeState == GamingModeState.Active;
        public string GamingModeButtonLabel => _gamingModeState switch
        {
            GamingModeState.Off          => "??? Activate Gaming Mode",
            GamingModeState.Activating   => "Activating???",
            GamingModeState.Active       => "??? Deactivate Gaming Mode",
            GamingModeState.Deactivating => "Deactivating???",
            _                            => "Gaming Mode"
        };

        public ObservableCollection<string> GamingLog { get; } = new();

        // ?????? Commands ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        public ICommand ToggleGamingModeCommand { get; }

        private async Task ToggleGamingModeAsync()
        {
            if (_gamingMode.State == GamingModeState.Off)
                await _gamingMode.ActivateAsync();
            else
                await _gamingMode.DeactivateAsync();
        }

        // ?????? Event Handlers ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        private void OnHardwareDataUpdated(object? sender, HardwareSnapshot snap)
        {
            // Always marshal to UI thread
            System.Windows.Application.Current.Dispatcher.Invoke(() =>
            {
                TotalCpuLoad    = snap.TotalCpuLoad;
                RamUsagePercent = snap.RamInfo.UsagePercent;
                RamUsageLabel   = $"{snap.RamInfo.UsedGb:F1} / {snap.RamInfo.TotalGb:F1} GB";

                // Sync per-core collection
                for (int i = 0; i < snap.CoreLoads.Count; i++)
                {
                    if (i < CoreLoads.Count)
                        CoreLoads[i].Load = snap.CoreLoads[i];
                    else
                        CoreLoads.Add(new CoreLoadItem { CoreIndex = i, Load = snap.CoreLoads[i] });
                }

                // GPU
                if (snap.GpuMetrics.Count > 0)
                {
                    GpuName        = snap.GpuMetrics[0].Name;
                    GpuTemperature = snap.GpuMetrics[0].TemperatureCelsius;
                    OnPropertyChanged(nameof(GpuTempLabel));
                }
            });
        }

        private void OnGamingModeStateChanged(object? sender, GamingModeState state)
        {
            System.Windows.Application.Current.Dispatcher.Invoke(() => GamingModeState = state);
        }

        // ?????? INotifyPropertyChanged ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        public event PropertyChangedEventHandler? PropertyChanged;

        private void OnPropertyChanged([CallerMemberName] string? name = null) =>
            PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(name));

        private void Set<T>(ref T field, T value, [CallerMemberName] string? name = null)
        {
            if (Equals(field, value)) return;
            field = value;
            OnPropertyChanged(name);
        }

        // ?????? IDisposable ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

        public void Dispose()
        {
            _engine.HardwareDataUpdated -= OnHardwareDataUpdated;
            _engine.Dispose();
        }
    }

    // ?????? Helper: per-core observable item ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????

    public class CoreLoadItem : INotifyPropertyChanged
    {
        public int CoreIndex { get; set; }

        private float _load;
        public float Load
        {
            get => _load;
            set
            {
                _load = value;
                PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(nameof(Load)));
                PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(nameof(LoadLabel)));
            }
        }

        public string LoadLabel => $"Core {CoreIndex}: {Load:F0}%";

        public event PropertyChangedEventHandler? PropertyChanged;
    }

    // ?????? AsyncRelayCommand ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

    public sealed class AsyncRelayCommand : ICommand
    {
        private readonly Func<Task> _execute;
        private          bool       _isExecuting;

        public AsyncRelayCommand(Func<Task> execute) => _execute = execute;

        public event EventHandler? CanExecuteChanged;

        public bool CanExecute(object? _) => !_isExecuting;

        public async void Execute(object? _)
        {
            _isExecuting = true;
            CanExecuteChanged?.Invoke(this, EventArgs.Empty);
            try   { await _execute(); }
            finally
            {
                _isExecuting = false;
                CanExecuteChanged?.Invoke(this, EventArgs.Empty);
            }
        }
    }
}



